package main

import "fmt"

func main() {
	// A declared variable of type 'string' without a value.
	var a string

	// A declared variable that has inferred its type from the value.
	var animal = "cat"

	// Shorthand variable declaration.
	fruit := "carrot"

	fmt.Println(a, animal, fruit)
}

/*
- Statically typed
- Strongly typed
*/
