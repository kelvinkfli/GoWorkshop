package main

import "fmt"

func main() {
	// A declaring a variable with a value.
	var a string

	// Assign that variable a value.
	a = "hello"

	// Declare a variable using shorthand declaration.
	fruit := "pineapple"

	// Declare multiple variables at once!
	number, boolean := 55, true

	fmt.Println(a, fruit, number, boolean)
}
